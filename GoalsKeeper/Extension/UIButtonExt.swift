extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.001499716542, green: 0.7995096445, blue: 0.6730570793, alpha: 0.6247056935)
    }
    
    func setDesecltedColor() {
        self.backgroundColor = #colorLiteral(red: 0.001499716542, green: 0.7995096445, blue: 0.6730570793, alpha: 1)
    }
}
